let num1 = parseInt(prompt("Ingrese un número"));

if(num1 % 2 === 0){
   document.write("<h2>El número " + num1 + " es divisible por 2</h2>");
} 
else if(num1 % 3 === 0){
   document.write("<h2>El número " + num1 + " es divisible por 3</h2>");
} 
else if(num1 % 5 === 0){
   document.write("<h2>El número " + num1 + " es divisible por 5</h2>");
} 
else if(num1 % 7 === 0){
   document.write("<h2>El número " + num1 + " es divisible por 7</h2>");
} 

